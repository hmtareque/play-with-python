#Python Dictionary
"""Dictionary is an unordered collection of key-value pairs."""

"""It is generally used when we have a huge amount of data. 
Dictionaries are optimized for retrieving data. We must know the key to retrieve the value."""

"""In Python, dictionaries are defined within braces {} with each item being a pair 
in the form key:value. Key and value can be of any type."""

d = {1:'value','key':2}
print(type(d))

print("d[1] = ", d[1])

print("d['key'] = ", d['key'])
